// Regenerates supabase/seed.sql from src/data/seed.json. Run this again
// whenever seed.json changes and the live project needs to catch up —
// seed.json stays the one source of truth; this script is just a projection
// of it into Postgres INSERT statements, never hand-edited independently.
//
//   node scripts/generate-supabase-seed.mjs > supabase/seed.sql

import { readFileSync } from "node:fs";

const model = JSON.parse(readFileSync(new URL("../src/data/seed.json", import.meta.url), "utf8"));

const sqlStr = (s) => `'${String(s).replace(/'/g, "''")}'`;
const sqlNum = (n) => (n == null ? "null" : String(n));
const sqlBool = (b) => (b ? "true" : "false");

const lines = [];
lines.push("-- Generated by scripts/generate-supabase-seed.mjs from src/data/seed.json.");
lines.push("-- Do not hand-edit — regenerate instead. Safe to re-run: every insert is");
lines.push("-- idempotent against the schema's natural keys.");
lines.push("");
lines.push("begin;");
lines.push("");

// clusters
lines.push("insert into clusters (id, name) values");
lines.push(
  model.clusters.map((c) => `  (${sqlStr(c.id)}, ${sqlStr(c.name)})`).join(",\n") + ""
);
lines.push("on conflict (id) do update set name = excluded.name;");
lines.push("");

// activities
lines.push("insert into activities (id, name, basis, uom, discipline, \"group\") values");
lines.push(
  model.activities
    .map(
      (a) =>
        `  (${sqlStr(a.id)}, ${sqlStr(a.name)}, ${sqlStr(a.basis)}, ${sqlStr(a.uom)}, ${sqlStr(a.discipline)}, ${sqlStr(a.group)})`
    )
    .join(",\n")
);
lines.push(
  "on conflict (id) do update set name = excluded.name, basis = excluded.basis, uom = excluded.uom, discipline = excluded.discipline, \"group\" = excluded.\"group\";"
);
lines.push("");

// layouts
lines.push("insert into layouts (id, name) values");
lines.push(model.layouts.map((l) => `  (${sqlStr(l.id)}, ${sqlStr(l.name)})`).join(",\n"));
lines.push("on conflict (id) do update set name = excluded.name;");
lines.push("");

// layout_unit_rates
const unitRateRows = model.layouts.flatMap((l) =>
  Object.entries(l.unit_rates).map(([activityId, rate]) => ({ layoutId: l.id, activityId, rate }))
);
lines.push("insert into layout_unit_rates (layout_id, activity_id, rate) values");
lines.push(
  unitRateRows.map((r) => `  (${sqlStr(r.layoutId)}, ${sqlStr(r.activityId)}, ${sqlNum(r.rate)})`).join(",\n")
);
lines.push("on conflict (layout_id, activity_id) do update set rate = excluded.rate;");
lines.push("");

// spaces — staged by (layout_id, space_key) so space_rates below can look
// up the generated uuid without the script needing to invent its own.
lines.push("insert into spaces (layout_id, space_key, name, floor_area, wall_area) values");
const spaceRows = model.layouts.flatMap((l) =>
  l.spaces.map((s) => ({ layoutId: l.id, spaceKey: s.id, name: s.name, floorArea: s.floor_area, wallArea: s.wall_area }))
);
lines.push(
  spaceRows
    .map(
      (s) =>
        `  (${sqlStr(s.layoutId)}, ${sqlStr(s.spaceKey)}, ${sqlStr(s.name)}, ${sqlNum(s.floorArea)}, ${sqlNum(s.wallArea)})`
    )
    .join(",\n")
);
lines.push(
  "on conflict (layout_id, space_key) do update set name = excluded.name, floor_area = excluded.floor_area, wall_area = excluded.wall_area;"
);
lines.push("");

// space_rates — resolved against spaces via an inline VALUES list rather
// than a session-scoped temp table. Supabase's SQL Editor doesn't guarantee
// a whole pasted script runs on one connection, so a `create temporary
// table` in one statement can be gone by the next (this is exactly the bug
// that surfaced running this against a real project: "relation
// space_rates_staging does not exist"). A single INSERT ... SELECT with the
// staging rows as a literal VALUES-derived table has no cross-statement
// state to lose.
const spaceRateRows = model.layouts.flatMap((l) =>
  l.spaces.flatMap((s) =>
    Object.entries(s.rates).map(([activityId, rate]) => ({
      layoutId: l.id,
      spaceKey: s.id,
      activityId,
      rate,
    }))
  )
);
lines.push("insert into space_rates (space_id, activity_id, rate)");
lines.push("select s.id, st.activity_id, st.rate");
lines.push("from (values");
lines.push(
  spaceRateRows
    .map(
      (r, i) =>
        `  (${sqlStr(r.layoutId)}, ${sqlStr(r.spaceKey)}, ${sqlStr(r.activityId)}, ${sqlNum(r.rate)})${i < spaceRateRows.length - 1 ? "," : ""}`
    )
    .join("\n")
);
lines.push(") as st(layout_id, space_key, activity_id, rate)");
lines.push("join spaces s on s.layout_id = st.layout_id and s.space_key = st.space_key");
lines.push("on conflict (space_id, activity_id) do update set rate = excluded.rate;");
lines.push("");

// buildings
lines.push("insert into buildings (id, code, name, building_type, cluster_id) values");
lines.push(
  model.buildings
    .map(
      (b) =>
        `  (${sqlStr(b.id)}, ${sqlStr(b.code)}, ${sqlStr(b.name)}, ${sqlStr(b.building_type)}, ${sqlStr(b.cluster_id)})`
    )
    .join(",\n")
);
lines.push(
  "on conflict (id) do update set code = excluded.code, name = excluded.name, building_type = excluded.building_type, cluster_id = excluded.cluster_id;"
);
lines.push("");

// building_units
const unitRows = model.buildings.flatMap((b) =>
  b.units.map((u) => ({ buildingId: b.id, layoutId: u.layout_id, count: u.count }))
);
if (unitRows.length > 0) {
  lines.push("insert into building_units (building_id, layout_id, count) values");
  lines.push(
    unitRows.map((u) => `  (${sqlStr(u.buildingId)}, ${sqlStr(u.layoutId)}, ${sqlNum(u.count)})`).join(",\n")
  );
  lines.push("on conflict (building_id, layout_id) do update set count = excluded.count;");
  lines.push("");
}

// lump_sums — no natural key in the source; re-running would duplicate, so
// clear a building's lump sums immediately before re-inserting them.
const lumpRows = model.buildings.flatMap((b) =>
  b.lump_sums.map((ls) => ({ buildingId: b.id, ...ls }))
);
lines.push("delete from lump_sums where building_id in (");
lines.push(model.buildings.map((b) => `  ${sqlStr(b.id)}`).join(",\n"));
lines.push(");");
lines.push("");
if (lumpRows.length > 0) {
  lines.push("insert into lump_sums (building_id, scope, discipline, item, amount, substantiated, note) values");
  lines.push(
    lumpRows
      .map(
        (ls) =>
          `  (${sqlStr(ls.buildingId)}, ${sqlStr(ls.scope)}, ${sqlStr(ls.discipline)}, ${sqlStr(ls.item)}, ${sqlNum(ls.amount)}, ${sqlBool(ls.substantiated)}, ${ls.note ? sqlStr(ls.note) : "null"})`
      )
      .join(",\n")
  );
  lines.push(";");
  lines.push("");
}

lines.push("commit;");
lines.push("");

process.stdout.write(lines.join("\n"));
